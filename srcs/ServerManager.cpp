void ServerManager::run() {
	std::vector<Server>::iterator it = this->_servers.begin();
	std::vector<Server>::iterator end = this->_servers.end();

	while(it != end)
	{
		try {
			*it.run(); // TODO add throw
			it++;
		}
		catch (std::exception e) {
		}
	}
}
