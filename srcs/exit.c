#include "../inc/minishell.h"

int 	ft_exit(t_all *a)
{
	if (!ft_strncmp("exit\0", a->cmd, 5))
	{
		if (a->arg)
			exit(ft_atoi(a->arg[0]));
		exit(1);
	}
	return (0);
}
