#include "../inc/minishell.h"

int 	pwd(char *str)
{
	char tmp[1024];

	if (cmd_itself("pwd", str))
	{
		getcwd(tmp, 1024);
		ft_putendl_fd(tmp, 1);
	}
	return (1);
}
